package psm.vo;

public class Attach {
	protected int fileNo;
	protected int tradeNo;
	
	public int getFileNo() {
		return fileNo;
	}
	public Attach setFileNo(int fileNo) {
		this.fileNo = fileNo;
		return this;
	}
	public int getTradeNo() {
		return tradeNo;
	}
	public Attach setTradeNo(int tradeNo) {
		this.tradeNo = tradeNo;
		return this;
	}
}
